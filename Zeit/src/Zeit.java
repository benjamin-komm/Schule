public class Zeit {

    private int Stunden;
    private int Minuten;

    public int getStunden() {
        return Stunden;
    }

    public void setStunden(int stunden) {
        Stunden = stunden;
    }

    public int getMinuten() {
        return Minuten;
    }

    public void setMinuten(int minuten) {
        Minuten = minuten;
    }
}
